    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int numero1 = Integer.valueOf(txtQuantidade.getText());
        int numero2 = Integer.valueOf(txtPrecoUni.getText());
        int resultado;
        
        if(txtDesconto.getText().equals("") || txtDesconto.getText().equals("0")){
            resultado = numero1 * numero2;
        }else{
            int numero3 = Integer.valueOf(txtDesconto.getText());
            resultado = numero1 * numero2 - numero3;
        }
       
        
        txtTotal.setText(String.valueOf(resultado));
    }//GEN-LAST:event_jButton1ActionPerformed

